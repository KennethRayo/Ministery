    private void jButton_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SaveActionPerformed
        // TODO add your handling code here:

        if (!"".equals(jTextField_IDChurch.getText()))
        {
                if (!"".equals(jTextField_Name.getText()))
                {
                        String IDChurch = jTextField_IDChurch.getText();
                        String Name = jTextField_Name.getText();
                        LB_District r =  (LB_District) this.jComboBox_ID_District.getSelectedItem();
                        String id = r.getID_District();

                        if (Actualizar == true)
                        {
                            //UPDATE

                            LB = new LB_Church(ID, Name, id);

                            Response = LB.Update();
                            if (LB.eye > 0)
                            {
                                JOptionPane.showMessageDialog(null, "Church " + LB.getID_church()+ "\n" + Response, "MF Control - Church", JOptionPane.INFORMATION_MESSAGE);
                                //listar
                                //this.dispose();
                            }
                            Actualizar = false;
                        }
                        else
                        {
                            //SAVE

                            LB = new LB_Church(IDChurch, Name, id);

                            //Insert CIF
                            String rptaRegistro = LB.Insert();

                            if (rptaRegistro != null)
                            {
                                JOptionPane.showMessageDialog(null, "Church " + IDChurch + "\n" + rptaRegistro, "MF Control - Church", JOptionPane.INFORMATION_MESSAGE);
                                //listar
                                //this.dispose();
                            }
                        }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "The Name Is Required !", "MF Control - Church", JOptionPane.WARNING_MESSAGE);
                }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "The ID Is Required !", "MF Control - Church", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton_SaveActionPerformed

