    private void jButton_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SaveActionPerformed
        // TODO add your handling code here:

        if (!"".equals(jTextField_IDReservation.getText()))
        {
                if (!"".equals(jTextField_Payment.getText()))
                {
                    if (jDateChooser_DateAndTime.getCalendar() != null)
                    {
                        String IDReservation = jTextField_IDReservation.getText();
                        
                        String PaymentStatus = jComboBox_PaymentStatus.getSelectedItem().toString();
                        String Payment = jTextField_Payment.getText();
                        TimeZone zone = TimeZone.getTimeZone("America/El_Salvador");
                        
                        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd - kk:mm:ss");
                        dateFormat.setTimeZone(TimeZone.getTimeZone("America/El_Salvador"));
                        String DateAndTime = dateFormat.format(jDateChooser_DateAndTime.getDate());

                        if (Actualizar == true)
                        {
                            //UPDATE

                            LB = new LB_Reservation(ID, PaymentStatus,
                                Float.valueOf(Payment), DateAndTime);

                            Response = LB.Update();
                            if (LB.eye > 0)
                            {
                                JOptionPane.showMessageDialog(null, "Reservation " + LB.getID_reservation()+ "\n" + Response, "MF Control - Reservation", JOptionPane.INFORMATION_MESSAGE);
                                //listar
                                //this.dispose();
                            }
                            Actualizar = false;
                        }
                        else
                        {
                            //SAVE

                            LB = new LB_Reservation(IDReservation, PaymentStatus,
                            Float.valueOf(Payment), DateAndTime);

                            //Insert CIF
                            String rptaRegistro = LB.Insert();

                            if (rptaRegistro != null)
                            {
                                JOptionPane.showMessageDialog(null, "Reservation " + IDReservation + "\n" + rptaRegistro, "MF Control - Reservation", JOptionPane.INFORMATION_MESSAGE);
                                //listar
                                //this.dispose();
                            }
                        }
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "The OpeningDate Is Required !", "MF Control - Reservation", JOptionPane.WARNING_MESSAGE);
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "The Payment Is Required !", "MF Control - Reservation", JOptionPane.WARNING_MESSAGE);
                }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "The ID Is Required !", "MF Control - Reservation", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton_SaveActionPerformed

